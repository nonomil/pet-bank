# 显式去重（保留首次出现顺序）
_seen = set()
TEXTS = [x for x in TEXTS if not (x in _seen or _seen.add(x))]

print(f"loading model from {MODEL} ...")
t0 = time.time()
model = VoxCPM.from_pretrained(MODEL, load_denoiser=False)
sr = model.tts_model.sample_rate
print(f"model loaded in {time.time()-t0:.1f}s, sample_rate={sr}")
print(f"total texts: {len(TEXTS)}")

mapping = {}
timings = []  # [(idx, len, seconds)]
for i, text in enumerate(TEXTS):
    key = _voice_clean(text)          # map 键：去空格对齐运行时清理
    h = hashlib.md5(key.encode("utf-8")).hexdigest()
    out = OUT / f"{h}.mp3"
    if out.exists() and out.stat().st_size > 100:
        mapping[key] = h
        continue  # 幂等跳过
    t = time.time()
    wav = model.generate(text=VOICE_PREFIX + text, cfg_value=2.0, inference_timesteps=10)  # 播报仍用原文
    sf.write(str(out), wav, sr)
    dt = time.time() - t
    mapping[key] = h
    timings.append((i, len(text), dt))
    print(f"[{i+1}/{len(TEXTS)}] {len(text)}字 {dt:.1f}s {h}")

with open(OUT / "map.json", "w", encoding="utf-8") as f:
    json.dump(mapping, f, ensure_ascii=False, indent=2)

# 打印耗时汇总（前若干条 + 平均）
if timings:
    print("\n=== 耗时统计（仅新生成）===")
    head = timings[:5] + ([(-1, 0, 0.0)] if len(timings) > 5 else [])
    for idx, n, dt in head:
        label = "..." if idx < 0 else f"#{idx+1}"
        print(f"  text{label} ({n}字): {dt}s")
    nums = [x[2] for x in timings]
    print(f"  最快={min(nums):.1f}s 最慢={max(nums):.1f}s 平均={sum(nums)/len(nums):.1f}s n={len(nums)}")
